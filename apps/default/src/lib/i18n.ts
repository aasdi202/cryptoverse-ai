// ─── Detect browser language ──────────────────────────────────────────────────
function detectLang(): LangCode {
  const saved = (localStorage.getItem('cryptoverse_lang') || localStorage.getItem('cryptoplay_lang')) as LangCode | null;
  if (saved && LANGUAGES.find(l => l.code === saved)) return saved;
  const browser = navigator.language.split('-')[0] as LangCode;
  if (LANGUAGES.find(l => l.code === browser)) return browser;
  return 'en';
}

// ─── Translation helper ───────────────────────────────────────────────────────
export function t(key: TKey, lang: LangCode): string {
  return TRANSLATIONS[lang]?.[key] ?? EN_STRINGS[key] ?? key;
}

export function isRTL(lang: LangCode): boolean {
  return LANGUAGES.find(l => l.code === lang)?.rtl ?? false;
}

export { detectLang };
